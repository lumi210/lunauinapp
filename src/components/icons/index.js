export function createSvgIcon(name, color = 'currentColor', size = 24) {
  const icon = getIcon(name)
  if (!icon) return ''
  
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="${icon.viewBox}" fill="none" stroke="${color}" stroke-width="${icon.strokeWidth}" stroke-linecap="round" stroke-linejoin="round"><path d="${icon.path}"/></svg>`
}

export function iconToBase64(name, color = '%23ffffff', size = 24) {
  const icon = getIcon(name)
  if (!icon) return ''
  
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="${icon.viewBox}" fill="none" stroke="${color}" stroke-width="${icon.strokeWidth}" stroke-linecap="round" stroke-linejoin="round"><path d="${icon.path}"/></svg>`
  
  return `data:image/svg+xml,${encodeURIComponent(svg)}`
}
